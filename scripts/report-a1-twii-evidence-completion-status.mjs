function summarizeSlot(id, outcome) {
  const classification = outcome?.classification ?? "missing";
  const pmQuestionResolved = outcome?.pmQuestionResolved === true;
  const status =
    classification === "accepted" && pmQuestionResolved
      ? "accepted_ready_for_outcome_gate"
      : classification === "needs_bounded_repair"
        ? "needs_bounded_repair"
        : ["blocked", "rejected", "unavailable"].includes(classification)
          ? "blocked_or_rejected"
          : "pending_no_secret_evidence";

  return {
    id,
    classification,
    pmQuestionResolved,
    status,
    requiredFields: requiredA1ReplyFields,
    nextA1ReplyShape: requiredA1ReplyFields.map((field) => `${field}: <${field}-for-${id}>`),
    remainingRisk: outcome?.remainingRisk ?? "A1 no-secret evidence has not been provided."
  };
}

function readOutcomes(filePath) {
  const parsed = JSON.parse(fs.readFileSync(filePath, "utf8"));
  if (!Array.isArray(parsed.outcomes)) throw new Error("Outcome file must include outcomes array");
  return parsed.outcomes;
}
